// Strict Equal to Operator
const we = 2;
console.log(we === 2); // true
console.log(we === '2'); // false


// Strict Not Equal to Operator
const se = 2, ze = 'hello';
console.log(se !== 2); // false
console.log(se !== '2'); // true
console.log(ze !== 'Hello'); // true


// Greater than Operator
const re = 3;
console.log(re > 2); // true


// Less than Operator
const as = 3, bs = 2;
console.log(as < 2); // false
console.log(bs < 3); // true


// Greater than or Equal to Operator
const an = 3;
console.log(an >= 3); //true


